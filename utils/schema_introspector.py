def _detect_join_key_mismatches(db_schemas: list[dict]) -> list[str]:
    """
    Compare column names and sample values across databases to detect
    potential join key format mismatches. Returns human-readable hints.

    Current heuristic: find columns with the same name across databases
    but different types or sample value formats.
    """
    hints = []
    col_index: dict[str, list[dict]] = {}

    for db in db_schemas:
        for table in db.get("tables", []):
            for col in table.get("columns", []):
                key = col["name"]
                col_index.setdefault(key, []).append({
                    "db": db.get("name", db["db_type"]),
                    "table": table["name"],
                    "type": col["type"],
                    "samples": col.get("sample_values", []),
                })

    for col_name, entries in col_index.items():
        if len(entries) < 2:
            continue
        types = {e["type"] for e in entries}
        if len(types) > 1:
            desc = " vs ".join(
                f"{e['db']}.{e['table']}.{col_name} ({e['type']}, "
                f"e.g. {e['samples'][0] if e['samples'] else 'no sample'})"
                for e in entries
            )
            hints.append(f"Potential mismatch on '{col_name}': {desc}")

    return hints


def _build_summary(db_schemas: list[dict], hints: list[str]) -> str:
    lines = ["## Schema Summary (for agent context injection)\n"]
    for db in db_schemas:
        name = db.get("name", db["db_type"])
        tables = [t["name"] for t in db.get("tables", [])]
        lines.append(f"- **{name}** ({db['db_type']}): tables/collections = {tables}")
    if hints:
        lines.append("\n### Join Key Mismatch Hints")
        for h in hints:
            lines.append(f"- {h}")
    return "\n".join(lines)


